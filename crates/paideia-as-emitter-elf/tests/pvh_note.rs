// Additional unit tests to increase coverage

#[test]
fn pvh_note_with_different_entry_addresses() {
    // Verify the encoder works with various entry addresses
    let addresses = [0x0, 0x100000, 0x200000, 0xffffffff];

    for addr in &addresses {
        let note = encode_pvh_note(*addr);
        assert_eq!(note.len(), 24, "All notes must be 24 bytes");
        assert_eq!(note.len() % 4, 0, "All notes must be 4-aligned");

        // Verify the address is encoded correctly in bytes 16-19
        let encoded_addr = u32::from_le_bytes([note[16], note[17], note[18], note[19]]);
        assert_eq!(
            encoded_addr, *addr,
            "Address {} not encoded correctly",
            addr
        );
    }
}

#[test]
fn pvh_note_name_alignment() {
    // Verify that the name is correctly aligned
    let note = encode_pvh_note(0x100000);

    // Name is at bytes 12-15
    assert_eq!(&note[12..16], b"Xen\0", "Name should be at bytes 12-15");

    // Verify the name is null-terminated
    assert_eq!(note[15], 0, "Name should be null-terminated");
}

#[test]
fn pvh_note_header_consistency() {
    // Verify header values are consistent across multiple calls
    let note1 = encode_pvh_note(0x100000);
    let note2 = encode_pvh_note(0x200000);

    // Both should have same header (only descriptor differs)
    assert_eq!(&note1[0..12], &note2[0..12], "Header should be identical");
    assert_ne!(&note1[16..20], &note2[16..20], "Descriptors should differ");
}

#[test]
fn writer_pvh_note_section_integration() {
    // Verify that the writer can successfully emit a PVH note in a relocatable object
    let mut writer = ElfWriter::new(Arch::X86_64, Kind::Relocatable);

    // Add minimal text and data
    writer.add_text_bytes(&[0x90, 0x90]); // Two NOPs
    writer.add_rodata_bytes(&[0xAA, 0xBB], 4);

    // Add PVH note
    let result = writer.add_pvh_note_section(0x100000);
    assert!(
        result.is_ok(),
        "add_pvh_note_section should succeed in relocatable objects"
    );

    // Finalize
    let elf_bytes = writer
        .finalize()
        .expect("finalize should succeed after adding PVH note");

    // Parse and verify
    let elf = object::read::elf::ElfFile64::<object::Endianness>::parse(elf_bytes.as_slice())
        .expect("should parse as valid ELF64");

    // Count sections
    let section_count = elf.sections().count();
    assert!(
        section_count >= 5,
        "ELF should have at least 5 sections (.text, .rodata, .data, .bss, .note.Xen)"
    );

    // Verify .note.Xen is present
    let has_pvh = elf
        .sections()
        .any(|s| s.name().unwrap_or("") == ".note.Xen");
    assert!(has_pvh, "ELF should contain .note.Xen section");
}
